# 메뉴창 관련
# Help -> Tip of the Day로 열 수 있음
# VCS -> Local History -> Show History : 코드 History, 코드 변화 내역 확인 및 복구 가능


# Ctrl 관련
# Ctrl + N (Navigate) : Class, 파일, symbol, action 등을 검색 하는 창 띄움
# Ctrl + F12 : class, method, 변수 등의 관계 리스트 확인 및 navigate기능
# Ctrl + B (Book) 또는 Ctrl + 마우스 클릭 :  클래스, 메서드가 정의된 파일 등을 열어볼 수 있는 기능
# Ctrl + Space 한번 또는 두번 클릭 : 자동완성 단어 드롭다운으로 띄우기, (클래스 또는 메서드를 선택하면 해당하는 모듈이 자동으로 import 됨)
# Ctrl + Q (Question) :  관련 문서, Example 보는 기능
# Ctrl + W (Wide) 또는 마우스 더블클릭:  코드 선택한 부분을 확장시키는 기능
# Ctrl + D (Duplicate) : 선택된 코드 복제
# Ctrl + P (Parameter) :  (괄호 사이에 커서 두고) 사용가능한 parameters 설명
# Ctrl + E (Explore) :  최근 열어본 파일 열기
# Ctrl + / : 주석 처리
# Ctrl + R (Replace) :  바꾸기 창


# Ctrl + Alt 관련
# Ctrl + Alt + v (Variables) : 자동으로 단어추출해서 간단히 변수 생성해서 바꿔줌
# Ctrl + Alt + S (Setting) : 설정창 열기


# Shift 관련
# Shift + f6 : 변수 한 번에 바꾸기, 바꾸고 싶은 변수 클릭 -> Shift+f6 -> 새로 열린 창에서 바꿔주면 됨
# Shift + 왼쪽마우스 클릭 또는 숨기고싶은 창에 커서 옮기고 -> 마우스 가운데 버튼 클릭하면 Hide 됨 : 불필요한 창 숨기기
# shift + enter : 즉시 줄바꿈 (자동완성무시)


# Ctrl + Shift 관련
# Ctrl + Shift + Up / Down : 선택된 코드 위/아래 이동
# Ctrl + Shift + J (Join) :  여러 줄 코드를 한 줄로 연결
# Ctrl + Shift + Backspace : 가장 최근에 변경한 코드 위치로 이동
# Ctrl + Shift + F7 : 변수에 하이라이트 표시
# Ctrl + Shift + v : 최근 사용 clipboard 열어서 붙여넣기 가능
# Ctrl + Shift + - : 모든 코드 접기 
# Ctrl + Shift + + : 모든 코드 펼치기


# Alt 관련
# Alt + F7 : 변수, class, method 등 사용된 위치 찾아주는 기능


# Alt + Shift 관련
# Alt + Shift + C (Change) : Project의 최근 변화 확인
# Alt + Shift + 마우스 클릭 드래그, Alt + 원하는 곳 클릭 : 마우스 커서 여러개 만들기
# Alt + Shift + F10 : Run/Debug 드롭다운 메뉴 바로 열기
# alt + shift + e :  코드 한 줄씩 실행


# Ctrl + Alt + shift 관련
# Ctrl + Alt + shift + J : 모든 동일한 단어 선택


